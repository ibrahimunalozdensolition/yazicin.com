// Markaları alfabetik sıralı olarak al ("Diğer" hariç)
export const getSortedBrands = (): string[] => {
  const brands = Object.keys(printerBrands).filter(b => b !== "Diğer").sort()
  return [...brands, "Diğer"]
}

// Belirli bir markanın modellerini al
export const getModelsByBrand = (brand: string): string[] => {
  return printerBrands[brand] || []
}

// Toplam marka sayısı
export const getTotalBrandCount = (): number => {
  return Object.keys(printerBrands).length
}

// Toplam model sayısı
export const getTotalModelCount = (): number => {
  return Object.values(printerBrands).reduce((total, models) => total + models.length, 0)
}

